class Command(BaseCommand):
    help = 'Seed reference data: directions, subsidy types, budgets, demo users'

    def handle(self, *args, **options):
        # Directions
        for code, name in DIRECTIONS:
            SubsidyDirection.objects.update_or_create(code=code, defaults={'name': name})
        self.stdout.write(f'Created {len(DIRECTIONS)} directions')

        # Application Periods (настраиваемые сроки приёма заявок)
        PERIODS = {
            # Скотоводство: 1 января — 31 декабря (расширено для демо)
            'cattle_meat': (1, 1, 12, 31, False),
            'cattle_dairy': (1, 1, 12, 31, False),
            'cattle_general': (1, 1, 12, 31, False),
            # Овцеводство: расширено
            'sheep': (1, 1, 12, 31, False),
            # Остальные — круглый год
        }
        period_count = 0
        for direction in SubsidyDirection.objects.all():
            period_data = PERIODS.get(direction.code)
            if period_data:
                sm, sd, em, ed, yr = period_data
                ApplicationPeriod.objects.update_or_create(
                    direction=direction,
                    defaults={
                        'start_month': sm, 'start_day': sd,
                        'end_month': em, 'end_day': ed,
                        'is_year_round': yr,
                    },
                )
                period_count += 1
            else:
                ApplicationPeriod.objects.update_or_create(
                    direction=direction,
                    defaults={'is_year_round': True},
                )
                period_count += 1
        self.stdout.write(f'Created {period_count} application periods')

        # Types
        created = 0
        for row in SUBSIDY_TYPES:
            dir_code, form_num, name, unit, rate, origin = row[:6]
            min_age = row[6] if len(row) > 6 else None
            max_age = row[7] if len(row) > 7 else None
            min_herd = row[8] if len(row) > 8 else None

            direction = SubsidyDirection.objects.get(code=dir_code)
            _, c = SubsidyType.objects.update_or_create(
                direction=direction,
                name=name,
                defaults={
                    'form_number': form_num,
                    'unit': unit,
                    'rate': rate,
                    'origin': origin,
                    'min_age_months': min_age,
                    'max_age_months': max_age,
                    'min_herd_size': min_herd,
                },
            )
            if c:
                created += 1
        self.stdout.write(f'Created/updated {len(SUBSIDY_TYPES)} subsidy types ({created} new)')

        # Budgets
        import random
        for region in REGIONS:
            for direction in SubsidyDirection.objects.all():
                amount = random.randint(50, 500) * 1_000_000
                Budget.objects.update_or_create(
                    year=2025,
                    region=region,
                    direction=direction,
                    defaults={
                        'planned_amount': amount,
                        'spent_amount': int(amount * random.uniform(0.3, 0.9)),
                    },
                )
        self.stdout.write(f'Created budgets for {len(REGIONS)} regions x {SubsidyDirection.objects.count()} directions')

